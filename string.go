package main

import "fmt"

func main() {
	fmt.Println("Hanif")
	fmt.Println("Hanif Fauzi") // this is string data type
	fmt.Println("Hanif Fauzi Hakim")

	// some function for string data type
	fmt.Println(len("Hanif"))
	fmt.Println("Hanif Fauzi"[0])       // 72 is byte code for 'H'
	fmt.Println("Hanif Fauzi Hakim"[1]) // 97 is byte code for "a"

}
